# Armazenando os valores em variáveis
a = 5
b = 2

# Imprimindo o tipo de dados das variáveis
print("Tipo de 'a':", type(a))
print("Tipo de 'b':", type(b))

# Realizando a divisão e armazenando o resultado
c = a / b

# Imprimindo o valor da terceira variável e seu tipo de dado
print("Valor de 'c':", c)
print("Tipo de 'c':", type(c))
